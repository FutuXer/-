#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：只给出了上下标尺，其它均需要自己补充
***************************************************************************/
int main()
{
	printf("请输入要打印的行数[3..13]\n");
	int n;
	scanf("%d", &n);
	/* 上标尺 */
	printf("--------------------------------------------------------------------------------\n");
	printf("0         1         2         3         4         5         6         7         \n");
	printf("01234567890123456789012345678901234567890123456789012345678901234567890123456789\n");
	printf("--------------------------------------------------------------------------------\n");
	int tri[13][13] = { 0 };
	for (int i = 0; i < n; i++)
	{
		tri[i][n - 1] = 1;
	}
	for (int i = 1; i < n; i++)//控制行
	{
		for (int j = 0; j < n-1; j++)
		{
			tri[i][j] = tri[i - 1][j] + tri[i - 1][j + 1];
		}
	}
	for (int i = 0; i < n; i++)
	{
		for (int j = 0; j < n; j++)
		{
			if (tri[i][j] == 0)
			{
				printf("      ");
			}
			else
			{
				printf("%6d", tri[i][j]);
			}
		}
		printf("\n");
	}


	/* 下标尺 */
	printf("--------------------------------------------------------------------------------\n");
	printf("01234567890123456789012345678901234567890123456789012345678901234567890123456789\n");
	printf("0         1         2         3         4         5         6         7         \n");
	printf("--------------------------------------------------------------------------------\n");
	printf("\n");

	return 0;
}